package com.array;

public class CharArray {
	public static void main(String[] args) {
		
		
		char []c={'s','h','a','n'};
		
	int	l=c.length;
	for(int i=0;i<l;i++) {
		System.out.print(c[i]);
		
		
	}
	System.out.println ("  ");
	for(int i=l-1;i>=0;i--) {
		
		System.out.print(c[i]);
		
	}
		
		
		
		
	}

}
