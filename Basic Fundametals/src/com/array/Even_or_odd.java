package com.array;

public class Even_or_odd {
	
	public static void main(String[] args) {
		
	
	
	
	int num [] = {25,22,27,29,78,29,89,56};
	
	
	for(int i=0;i<num.length;i++) {
		
		if(num[i]%2==0) {
			
			System.out.println("it is even number0"+num[i]);
		}else {
			
			System.out.println("it is odd number"+num[i]);
		}
		
		
	}
	
	
	}
}
