package com.dataTypes;

public class AllDataTypeRanges {
	public static void main(String[] args ) {
		
		///integers
		
		byte a=127;
		System.out.println("maximum range of byte "+Byte.MAX_VALUE);
		System.out.println("manimum range of byte "+Byte.MIN_VALUE);
		
		System.out.println("----------------------------------------------");
		
		short b=30000;
		
	System.out.println("Maximum range of short  "+Short.MAX_VALUE);
	System.out.println("Manimum range of short  "+Short.MIN_VALUE);
	
	System.out.println("----------------------------------------------");
	
	int c=454656232;
	
	System.out.println("Maximum range of int "+Integer.MAX_VALUE);
	System.out.println("Manimum range of int "+Integer.MIN_VALUE);
	
	System.out.println("----------------------------------------------");
	
	long d=6636659696366965L ;
	System.out.println("Maximum range of long : "+Long.MAX_VALUE);
	System.out.println("Manimum range of long : "+Long.MIN_VALUE);
	
	System.out.println("----------------------------------------------");
//floating point 
	
	
	float a1=55.5465666556f;
	System.out.println("Maximum range of float : "+Float.MAX_VALUE);
	System.out.println("Manimum range of float : "+Float.MIN_VALUE);
	
	System.out.println("----------------------------------------------");
	
	double b1=555.45451216454964d;
	System.out.println("Maximum range of double : "+Double.MAX_VALUE);
	System.out.println("Manimum range of double : "+Double.MIN_VALUE);
	
	System.out.println("----------------------------------------------");
	
	boolean a2=true; // boolean range only true or false
	System.out.println(a2);
	
	System.out.println("----------------------------------------------");
	
	char a3='a'; ///charecter range only single charecter
	
	System.out.println(a3);
	

	}

}
