package com.ifelsePrograms;

public class A_number_is_positive_negative_or_zero {
	public static void main(String [] args) {
		int a =7;
		if(0<a) {
			System.out.println("the number is positive "+a);
		}else if(0>a) {
			System.out.println("the number is negative "+a);
			
		}else if(a==0) {
			System.out.println("the number is zero "+0);
		}
		
	}

}
