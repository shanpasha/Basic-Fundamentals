package com.number_system;

public class Swaping {
	public static void main(String []args) {
		
		int a=4;
		int b=5;
		int c=7;
		
		System.out.println("before swapping");
		System.out.println("a = "+a);
		System.out.println("b = "+b);
		int d=a;
		a=b;
		b=d;
		
		System.out.println("after swapping");
		System.out.println("a = "+a);
		System.out.println("b = "+b);
		
	}
	

}
