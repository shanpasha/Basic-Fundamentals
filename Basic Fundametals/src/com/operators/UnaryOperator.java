package com.operators;

public class UnaryOperator {
public static void main(String []args) {
	int a=10;
	int b=10;
	
	System.out.println("post increemnt");
	System.out.println("a++= "+a++);
	System.out.println("a++= "+ a);
	
	System.out.println("pre increement");
	System.out.println("++b= "+ ++b);
	System.out.println("++b= "+ b);
	

	
	
}
}
